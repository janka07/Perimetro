using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Area_y_Perimetro
{
    class cuadrilatero
    {
        private readonly double lado1;
        private readonly double lado2;
        public cuadrilatero(double_lado1 double_lado2);

        lado1 = private readonly double_lado1
        lado2 = _lado2;
        private static double_lado1 _lado2;
    }
    public double Perimetro(lado1 lado2) => lado1 + lado2 + 2;
    public double area ()
    {
        return lado1 * lado2;

    }

    


    


